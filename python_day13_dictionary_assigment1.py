#Write a Python program and calculate the mean of the below dictionary.
# Dictionary with values
test_dict = {"A": 6, "B": 9, "C": 5, "D": 7, "E": 4}

# Calculate the sum of values
total = sum(test_dict.values())

# Find the number of items
count = len(test_dict)

# Calculate the mean
mean = total / count

# Print the result
print(mean)

'''output
6.2'''
